CREATE TABLE TT_FIXEDRULE
(PAYRULE VARCHAR(100),
LENGTH VARCHAR(50),
STARTDAY VARCHAR(40),
STARTDATE VARCHAR(20),
DAYDIVIDE VARCHAR(10));


CREATE TABLE TT_DEDUCTION
(PAYRULE VARCHAR(100),
DEDUCTION  VARCHAR(100))

CREATE TABLE TT_MEAL
(PAYRULE VARCHAR(100),
MEAL  VARCHAR(100))

CREATE TABLE TT_PAYRULE
(PAYRULE VARCHAR(100),
WORKRULE  VARCHAR(100))

CREATE TABLE TT_WORKRULE
(WORKRULE  VARCHAR(100),
ROUNDINGRULE VARCHAR(100),
PCD VARCHAR(100))

CREATE TABLE TT_ZONES
(PCDNAME VARCHAR(100),
ZONE VARCHAR(100))
----------------------------
--2.1.2
SELECT LENGTH, STARTDAY, STARTDATE, DAYDIVIDE, PAYRULE
FROM TT_FIXEDRULE
ORDER BY LENGTH, STARTDAY, STARTDATE, DAYDIVIDE, PAYRULE


SELECT ROUNDINGRULE, PAYRULE FROM TT_WORKRULE, TT_PAYRULE
WHERE
TT_WORKRULE.WORKRULE = TT_PAYRULE.WORKRULE
ORDER BY ROUNDINGRULE, PAYRULE

select meal, payrule from TT_MEAL
order by meal

select DEDUCTION, payrule
from TT_DEDUCTION
order by DEDUCTION

select ZONE, PAYRULE from TT_PAYRULE P, TT_WORKRULE W, TT_ZONES Z
WHERE
P.WORKRULE = W.WORKRULE AND
W.PCD = Z.PCDNAME
ORDER BY ZONE, PAYRULE
